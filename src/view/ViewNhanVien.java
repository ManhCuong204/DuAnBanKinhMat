    public void fillToTable(List<NhanVien> list) {
        String[] cols = {"Id", "Email", "Trạng thái", "Địa chỉ", "Tên", "Tuổi", "Số điện thoại", "Chức vụ"};
        tableModel.setColumnIdentifiers(cols);
        tableModel.setRowCount(0);

        for (NhanVien nv : list) {
            tableModel.addRow(new Object[]{
                nv.getId(),
                nv.getEmail(),
                nv.getTrangThai(),
                nv.getDiaChi(),
                nv.getTenNV(),
                nv.getTuoiNV(),
                nv.getSdt(),
                nv.getChucVu()
            });
        }
    }

    public void fillToForm(int indexxx) {
        NhanVien nv = listNV.get(indexxx);
        txtMaNV.setText(String.valueOf(nv.getId()));
        txtEmail.setText(nv.getEmail());
        txtTrangThai.setText(nv.getTrangThai());
        txtDiaChiNV.setText(nv.getDiaChi());
        txtTenNV.setText(nv.getTenNV());
        txtTuoiNV.setText(String.valueOf(nv.getTuoiNV()));
        txtSdtNV.setText(String.valueOf(nv.getSdt()));
        txtChucVu.setText(nv.getChucVu());
    }

    public NhanVien getNhanVien() {
        try {
            // Kiểm tra dữ liệu không được để trống
            if (txtTenNV.getText().isEmpty() || txtEmail.getText().isEmpty()
                    || txtTrangThai.getText().isEmpty() || txtDiaChiNV.getText().isEmpty()
                    || txtTuoiNV.getText().isEmpty() || txtSdtNV.getText().isEmpty() || txtChucVu.getText().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin.");
                return null;
            }

            // Kiểm tra định dạng số cho tuổi và số điện thoại
            int tuoi, sdt;
            try {
                tuoi = Integer.parseInt(txtTuoiNV.getText());
                sdt = Integer.parseInt(txtSdtNV.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Nhập giá trị số không hợp lệ.");
                return null;
            }

            // Kiểm tra định dạng email
            String emailRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
            if (!txtEmail.getText().matches(emailRegex)) {
                JOptionPane.showMessageDialog(this, "Định dạng email không hợp lệ.");
                return null;
            }

            // Tạo đối tượng NhanVien
            int id = (txtMaNV.getText().isEmpty()) ? 0 : Integer.parseInt(txtMaNV.getText());
            NhanVien nv = new NhanVien(
                    id,
                    txtEmail.getText(),
                    txtTrangThai.getText(),
                    txtDiaChiNV.getText(),
                    txtTenNV.getText(),
                    tuoi,
                    sdt,
                    txtChucVu.getText()
            );

            return nv;
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Nhập giá trị số không hợp lệ.");
            return null;
        }
    }

