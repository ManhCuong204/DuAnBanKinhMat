    private void txtSearchNVDangLamKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchNVDangLamKeyReleased
        // TODO add your handling code here:
        String trangThaiToSearch = txtSearchNVDangLam.getText().trim(); // Lấy giá trị từ TextField và loại bỏ khoảng trắng

        // Kiểm tra nếu chuỗi tìm kiếm không rỗng
        if (!trangThaiToSearch.isEmpty()) {
            DefaultTableModel tblModel = (DefaultTableModel) tblNhanVienDL.getModel();
            tblModel.setRowCount(0); // Xóa dữ liệu cũ trên bảng

            // Lặp qua danh sách nhân viên và hiển thị những nhân viên thỏa mãn điều kiện tìm kiếm
            for (NhanVien nhanVien : listNV) {
                // Kiểm tra nếu chucVu của NhanVien chứa chuỗi tìm kiếm (không phân biệt hoa thường)
                if (nhanVien.getTrangThai().toLowerCase().contains(trangThaiToSearch.toLowerCase())) {
                    // Thêm dòng mới với thông tin của NhanVien tìm được
                    Object[] rowData = {
                        nhanVien.getId(),
                        nhanVien.getEmail(),
                        nhanVien.getTrangThai(),
                        nhanVien.getDiaChi(),
                        nhanVien.getTenNV(),
                        nhanVien.getTuoiNV(),
                        nhanVien.getSdt(),
                        nhanVien.getChucVu()
                    };
                    tblModel.addRow(rowData);
                }
            }

            // Cập nhật model cho bảng
            tblNhanVienDL.setModel(tblModel);
        } else {
            // Nếu chuỗi tìm kiếm rỗng, có thể thực hiện các hành động khác tùy thuộc vào yêu cầu của bạn
            // ở đây là in thông báo ra console
            fillToTable(listNV);
            System.out.println("Please enter a value to search.");
        }
    }//GEN-LAST:event_txtSearchNVDangLamKeyReleased

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        // Lấy thông tin từ các ô text field hoặc các thành phần giao diện khác
        NhanVien updatedNhanVien = getNhanVien();
        if (updatedNhanVien != null) {
            NhanVienRepository nhanVienRepository = new NhanVienRepository();
            int result = nhanVienRepository.updateNhanVien(updatedNhanVien);

            if (result > 0) {
                JOptionPane.showMessageDialog(this, "Update thành công !!");
                System.out.println("NhanVien updated successfully.");
            } else {
                JOptionPane.showMessageDialog(this, "Update thất bại !!");
                System.out.println("Failed to update NhanVien.");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

